void memory_inf(struct terminal *term, void *d, struct session *ses)
{
	char message[2048];
	char *p;
	struct refresh *r;

	r = mem_alloc(sizeof(struct refresh));
	if (!r) return;

	snprintf(message, sizeof(message), "%ld %s.",
		 mem_amount, _(N_(T_MEMORY_ALLOCATED), term));

	p = stracpy(message);
	if (!p) {
		mem_free(r);
		return;
	}

	msg_box(term, getml(p, NULL),
		N_(T_MEMORY_INFO), AL_CENTER,
		p,
		r, 1,
		N_(T_OK), NULL, B_ENTER | B_ESC);

	refresh_init(r, term, ses, d, memory_inf);
}

